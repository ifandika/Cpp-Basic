#include <iostream>
using namespace std;

int main(){
  
  // Operasi increment & decrement
  //
  // ++  ( Menambah nilai dengan 1 )
  // --  ( Mengurangi nilai dengan 1 )
  
  // Deklarasi tipe data
  int nilai1 = 10;
  int nilai2 = 6;
  
  // Penjumlahan
  nilai1++;
  cout << "Penjumlahan " << nilai1 << endl;
  
  // Pengurangan
  nilai2--;
  cout << "Pengurangan " << nilai2 << endl;
  
  cin.get();
  return 0;
}