#include <iostream>

using namespace std;

int main(){
  
  // Variabel = Nilai / data yang tidak tetap dan dapat diubah
  
  // Deklarasi = Mendeklarasikan Sebuah Data / Variabel
  // contoh => int A;
  
  // Kita mendeklarasikan a adalah int (integer / bilangan bulat)
  int a = 0; // simbol = ( Assignment )
  
  // Inisialisasi = mendeklarasikan Data Dengan Memasukan nilai
  // contoh => int B = 10;
  
  cout << "nilai a awal : " << a << endl;
  a = 20;// Akan mengganti nilai a menjadi 20
  
  cout << "nilai a akhir : " << a << endl;
  
  cin.get();
  return 0;
}